const n=`# Bagels\r
\r
## Notes\r
Makes 6 large or 8 med. Probs 8 if using sourdough starter instead of yeast. Can consider cold fermenting overnight after bagels have been shaped.  \r
\r
## Ingredients\r
### Main\r
Yeast (instant dry),  1 pack  \r
Water,  275g (anywhere 55-60% hydration)  \r
Salt,  10g (2%)  \r
Flour,  500g  \r
Honey or Barley Malt Syrup, 20g  \r
Baking soda,  1 Tbsp  \r
Egg,  1  \r
\r
### Other\r
Everything bagel seasoning  \r
\r
## Method\r
1. Bloom yeast with honey  \r
2. Mix in salt a flour. Knead until just combined. Let rest for 5 mins then knead for 8-10 mins  \r
3. Let rise until doubled (around 1hr)  \r
4. Punch down and divide in 6-8 pieces. Shape into boules and let rest for 10 mins  \r
5. Lightly oil (or dust with cornmeal) a baking tray and shape bagels. Proof until risen (45-60 mins or overnight in fridge)  \r
6. Boil large pot of water (about 2L) and preheat oven to 250C. Add salt, baking soda and honey (or malt or molasses) to water  \r
7. Boil each side for 1 min 30 sec and drain over wire rack  \r
8. Brush with egg wash then put on toppings.  \r
9. Bake for 15 mins  \r
10. Cool for 10 mins before cutting  `;export{n as default};
